// ? 22. Generate Parentheses
// ? https://leetcode.com/problems/generate-parentheses/description/

#include <iostream>
#include <bits/stdc++.h>
#include <string>

using namespace std;

vector<string> ans;

void addBrackets(int open, int close, string s)
{
    if (open == 0 & close == 0)
    {
        ans.emplace_back(s);
        return;
    }

    if (open == close)
    {
        addBrackets(open - 1, close, s + "(");
    }
    else
    {
        if (open != 0)
        {
            addBrackets(open - 1, close, s + "(");
        }
        addBrackets(open, close - 1, s + ")");
    }
}

vector<string> generateParenthesis(int n)
{
    addBrackets(n, n, "");
    return ans;
}

